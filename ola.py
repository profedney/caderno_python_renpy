a = 12  # Atribui o valor 12 à variável 'a'
b = 3   # Atribui o valor 3 à variável 'b'

somar = a + b  # Soma 'a' e 'b' e armazena o resultado em 'somar'
sub = a - b    # Subtrai 'b' de 'a' e armazena o resultado em 'sub'
mult = a * b   # Multiplica 'a' por 'b' e armazena o resultado em 'mult'
div = a / b    # Divide 'a' por 'b' e armazena o resultado em 'div'

# Exibe os resultados das operações
print("A Soma é:", somar)
print("A Subtração é:", sub)
print("A Multiplicação é:", mult)
print("A Divisão é:", div)
